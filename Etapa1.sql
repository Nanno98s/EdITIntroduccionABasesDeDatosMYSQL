CREATE TABLE FACTURAS(Letra CHAR,Numero INT,ClienteID INT,
ArticuloID INT,Fecha DATE, Monto DOUBLE, PRIMARY KEY(Letra,Numero));
DESCRIBE FACTURAS;


CREATE TABLE ARTICULOS(ArticuloID INT PRIMARY KEY,Nonmbre VARCHAR(50),
Precio DOUBLE, Stock INT);
DESC ARTICULOS;


CREATE TABLE CLIENTE(ClienteID INT PRIMARY KEY,Nombre VARCHAR(25),
Apellido VARCHAR(25),CUIT CHAR(16),Direccion VARCHAR(50),
Comentarios VARCHAR(50));
DESCRIBE CLIENTE;


DESCRIBE FACTURAS;
